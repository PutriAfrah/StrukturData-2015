public class Vector3D {

    double x,y,z;

    public double x()
    {
        return x;
    } 

    public double y()
    {
        return y;
    }

    public double z()
    {
        return z;
    }

    public Vector3D (double x, double y, double z) {
        this.x=x;
        this.y=y;
        this.z=z;

    }
}
